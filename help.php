Applicazione web gratuita progettata e realizzata dai soci di ISF. 
Pensata per restituire la parola in modo semplice ed immediato a chi per malattia perde
temporaneamente o definitivamente la possibilità di parlare e muoversi.
L’utente può scaricare un sistema di puntamento a pupilla (software consigliati nell'apposita sezione)
e utilizzare uno dei servizi disponibili.
SpeechBoard (base): tastiera virtuale con lettere, numeri e punteggiatura base
SpeechBoard : tastiera virtuale con lettere maiuscole e minuscole, numeri, punteggiatura
WordBoard : tastiera virtuale con parole già predefinite
ImageBoard : tastiera virtuale con immagini
L'utente può quindi comporre o scegliere una parola o immagine in base e verrà pronunciato il testo corrispondente
per mezzo della sintesi vocale.
L’obiettivo del progetto è di offrire a tutti, anche ai non esperti, uno strumento facile da utilizzare, gratuito e che
non lasci nessuno nel silenzio, dando la possibilità a chiunque viva situazioni così difficili di pensare… 
“Io parlo di nuovo”

